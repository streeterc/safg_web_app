    <p><strong>Last Updated: May 14, 2025</strong></p>

    <p>At Strategic Alliance Financial Group Inc, we are committed to ensuring that our website is accessible to all
        users, including those
        with disabilities. We strive to comply with the <em>Accessibility for Ontarians with Disabilities Act</em>
        (AODA) and the <em>Web Content Accessibility Guidelines</em> (WCAG) 2.1 Level AA to provide an inclusive online
        experience for our Canada-wide audience.</p>

    <h2>Our Accessibility Features</h2>
    <p>Our website is designed with the following accessibility features:</p>
    <ul>
        <li><strong>Keyboard Navigation</strong>: Users can navigate the site using a keyboard alone.</li>
        <li><strong>Screen Reader Compatibility</strong>: We use proper heading structures and ARIA landmarks to assist
            screen reader users.</li>
        <li><strong>Form Accessibility</strong>: All forms are labeled correctly for assistive technologies.</li>
        <li><strong>Color Contrast</strong>: Text and background colors meet WCAG contrast standards for readability.
        </li>
        <li><strong>Text Alternatives</strong>: Images and non-text content include appropriate alt text or
            descriptions.</li>
    </ul>

